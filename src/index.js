const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};

function decode(expr) {
    let n = 0;
    let finalString = "";

    while (n < expr.length) {
        finalString += MORSE_TABLE[convertNumbersToMorse(Number(expr.substr(n, 10)).toString())] || " ";
        n += 10;
    }

    return finalString;
}

function convertNumbersToMorse(stringifiedNumber) {
    return stringifiedNumber.replace(/10/gi, ".").replace(/11/gi, "-");
}

module.exports = {
    decode
}